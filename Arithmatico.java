import java.util.*;
public class Arithmatico 
{
  public static void main(String args[])  
  {
      int a,b;
      Scanner Sc=new Scanner(System.in);
      System.out.println("Enter A: ");
      a=Sc.nextInt();
      System.out.println("Enter B: ");
      b=Sc.nextInt();
      
     System.out.println("A+B= "+(a+b)); 
     System.out.println("A-B= "+(a-b)); 
     System.out.println("A*B= "+(a*b)); 
     System.out.println("A/B= "+(a/b)); 
              
  }
}

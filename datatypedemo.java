
public class datatypedemo 
{
   public static void main(String args[])
   {
       int a;
       a=10;
       System.out.println("Int value a="+a);
       
       float b;
       b=2.34f;
       System.out.println("float value b="+b);
       
       double c;
       c=88.45;
       System.out.println("double value c="+c);
       
       char d;
       d='j';
       System.out.println("char value d="+d);
       
       String name;
       name="arshima";
       System.out.println("String value name="+name);
       
       boolean x;
       x=true;
       System.out.println("x="+x);
       
       
       
       
   }
           
}
